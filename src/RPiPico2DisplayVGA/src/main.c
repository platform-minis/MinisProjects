// ---------------------------------------------------------------------------
// Framebuffer — operacje pikselowe
// ---------------------------------------------------------------------------
static inline void fb_set_pixel(uint32_t x, uint32_t y, bool on) {
    if (x >= FB_WIDTH || y >= FB_HEIGHT) return;
    uint8_t *b = &framebuf[y * FB_STRIDE + x / 8u];
    uint8_t  m = 0x80u >> (x % 8u);
    if (on) *b |= m;
    else    *b &= ~m;
}

// ---------------------------------------------------------------------------
// Renderowanie tekstu 80×60 znaków (8×8 px / znak)
// ---------------------------------------------------------------------------
#define TEXT_COLS  (FB_WIDTH  / 8u)   // 80
#define TEXT_ROWS  (FB_HEIGHT / 8u)   // 60

static uint32_t cur_col = 0;
static uint32_t cur_row = 0;

static void fb_draw_char(uint32_t col, uint32_t row, char c) {
    if ((uint8_t)c >= 128u) return;
    const uint8_t *glyph = font8x8[(uint8_t)c];
    uint32_t px = col * 8u;
    uint32_t py = row * 8u;
    for (uint32_t r = 0; r < 8u; r++) {
        uint8_t bits = glyph[r];
        for (uint32_t p = 0u; p < 8u; p++)
            fb_set_pixel(px + p, py + r, (bits >> (7u - p)) & 1u);
    }
}

static void fb_scroll_up(void) {
    memmove(&framebuf[0],
            &framebuf[8u * FB_STRIDE],
            (TEXT_ROWS - 1u) * 8u * FB_STRIDE);
    memset(&framebuf[(TEXT_ROWS - 1u) * 8u * FB_STRIDE], 0, 8u * FB_STRIDE);
}

static void fb_put_char(char c) {
    if (c == '\n') {
        cur_col = 0;
        cur_row++;
    } else if (c == '\r') {
        cur_col = 0;
    } else if (c == '\b') {
        if (cur_col > 0) {
            cur_col--;
            fb_draw_char(cur_col, cur_row, ' ');
        }
    } else {
        fb_draw_char(cur_col, cur_row, c);
        cur_col++;
        if (cur_col >= TEXT_COLS) {
            cur_col = 0;
            cur_row++;
        }
    }
    if (cur_row >= TEXT_ROWS) {
        fb_scroll_up();
        cur_row = TEXT_ROWS - 1u;
    }
}

static void fb_puts(const char *s) {
    while (*s) fb_put_char(*s++);
}

// ---------------------------------------------------------------------------
// Core 1 — sterownik PicoVGA (blokuje, generuje VGA przez PIO0 + DMA)
// ---------------------------------------------------------------------------
static void core1_entry(void) {
    VgaCore();
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
int main(void) {
    stdio_init_all();

    // Wyczyść framebuffer przed startem wideo
    memset(framebuf, 0, sizeof(framebuf));

    // Uruchom sterownik VGA na core 1 (musi być przed Video())
    multicore_launch_core1(core1_entry);

    // Inicjalizacja PicoVGA (PIO0):
    //   DEV_VGA     — wyjście VGA (GP0–GP9)
    //   RES_VGA     — 640×480 @ 60 Hz
    //   FORM_GRAPH1 — format 1bpp (MSB = lewy piksel)
    Video(DEV_VGA, RES_VGA, FORM_GRAPH1, framebuf, NULL);

    // Inicjalizacja klawiatury PS/2 (PIO1, GP20=DATA, GP21=CLK)
    kbd_init(1, PS2_DAT_GPIO);

    fb_puts("RPiPico2DisplayVGA\n");
    fb_puts("PS/2 keyboard ready. Type below:\n");
    fb_puts("> ");

    while (true) {
        if (kbd_ready()) {
            char c = kbd_getc();
            // Echo na monitor VGA
            fb_put_char(c);
            // Echo przez USB serial (minicom/screen na /dev/ttyACM*)
            putchar_raw(c);
            // Po Enter — nowa linia z promptem
            if (c == '\n') fb_puts("> ");
        }
    }
}
